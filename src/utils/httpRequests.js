import axios from 'axios';

const post = (config) => {
  if (config) {
    return axios({
      url: config.url,
      method: 'POST',
      data: config.data,
      Headers: []
    });
  }
};

const get = (config) => {
  if (config) {
    return axios({
      url: config.url,
      method: 'GET',
      Headers: []
    });
  }
};

export {
  post, get
};
